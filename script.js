const applySeo = (lang) => {
  const page = getCurrentPage();
  const entry = seoMap[page];
  const localeValue = lang === "es" ? "es_ES" : "en_US";
  let ogLocale = document.querySelector("meta[property='og:locale']");
  if (!ogLocale) {
    ogLocale = document.createElement("meta");
    ogLocale.setAttribute("property", "og:locale");
    document.head.appendChild(ogLocale);
  }
  ogLocale.setAttribute("content", localeValue);

  let ogLocaleAlt = document.querySelector("meta[property='og:locale:alternate']");
  if (!ogLocaleAlt) {
    ogLocaleAlt = document.createElement("meta");
    ogLocaleAlt.setAttribute("property", "og:locale:alternate");
    document.head.appendChild(ogLocaleAlt);
  }
  ogLocaleAlt.setAttribute("content", lang === "es" ? "en_US" : "es_ES");

  if (!entry) return;
  const meta = entry[lang] || entry.en;
  if (!meta) return;
  if (meta.title) {
    document.title = meta.title;
    const ogTitle = document.querySelector("meta[property='og:title']");
    const twTitle = document.querySelector("meta[name='twitter:title']");
    if (ogTitle) ogTitle.setAttribute("content", meta.title);
    if (twTitle) twTitle.setAttribute("content", meta.title);
  }
  if (meta.description) {
    const desc = document.querySelector("meta[name='description']");
    const ogDesc = document.querySelector("meta[property='og:description']");
    const twDesc = document.querySelector("meta[name='twitter:description']");
    if (desc) desc.setAttribute("content", meta.description);
    if (ogDesc) ogDesc.setAttribute("content", meta.description);
    if (twDesc) twDesc.setAttribute("content", meta.description);
  }
};

