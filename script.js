document.querySelectorAll("form[data-async-form='true']").forEach((form) => {
  const btn = form.querySelector("button[type='submit']");
  const status = form.querySelector("[data-form-status]");
  upsertHiddenField(form, "website", "");
  form.addEventListener("submit", async (e) => {
    e.preventDefault();
    if (!btn || !status) return;
    const lang = root.getAttribute("lang") || "en";
    const dict = i18n[lang] || i18n.en;
    const defaultLabel = dict["form.submit"] || btn.textContent;
    btn.disabled = true;
    btn.textContent = dict["form.sending"];
    status.classList.remove("is-success", "is-error");
    status.textContent = dict["form.sending"];
    try {
      const honeypot = form.querySelector("[name='website']")?.value || "";
      if (honeypot.trim()) throw new Error("bot-detected");
      if (!ensureTurnstileToken(form)) throw new Error("turnstile-required");

      const now = Date.now();
      const last = Number(localStorage.getItem("cvLastSubmitTs") || "0");
      if (last && now - last < 6000) throw new Error("rate-limited");
      localStorage.setItem("cvLastSubmitTs", String(now));

      if (form.hasAttribute("data-booking-form")) {
        const preferredSlot = form.querySelector("[name='preferred_slot']")?.value || "";
        if (!preferredSlot) throw new Error("slot-required");
      }
      computeLeadRouting(form);
      attachAttributionFields(form);
      const formData = new FormData(form);

      const target = form.getAttribute("data-webhook-target") || "crm";
      const customWebhook = form.getAttribute("data-webhook-url") || "";
      const webhook = customWebhook || (
        target === "marketing"
          ? (window.CV_MARKETING_WEBHOOK || document.querySelector("meta[name='cv-marketing-webhook']")?.content || "")
          : (window.CV_CRM_WEBHOOK || document.querySelector("meta[name='cv-crm-webhook']")?.content || "")
      );

      const submitEndpoint = form.getAttribute("data-submit-endpoint") || (form.hasAttribute("data-netlify") ? "/" : "");
      let delivered = false;

      if (webhook) {
        const webhookRes = await fetch(webhook, {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify(Object.fromEntries(formData.entries()))
        });
        if (webhookRes.ok) delivered = true;
      }

      if (submitEndpoint) {
        const endpointRes = await fetch(submitEndpoint, {
          method: "POST",
          headers: { "Content-Type": "application/x-www-form-urlencoded" },
          body: encodeForm(formData)
        });
        if (endpointRes.ok) delivered = true;
      }

      if (!delivered) throw new Error("submit-failed");

      const formName = form.getAttribute("name") || "contact";
      const stageMap = {
        "contact": "lead_submitted",
        "booking-intake": "meeting_requested",
        "secure-checkout": "checkout_requested",
        "newsletter-subscribe": "briefing_subscribed",
        "partner-application": "partner_application",
        "preferences-center": "preferences_updated"
      };
      const funnelStage = stageMap[formName] || "lead_submitted";
      trackEvent("funnel_stage", { stage: funnelStage, form_name: formName, lead_source: String(formData.get("lead_source") || "") });
      if (funnelStage === "briefing_subscribed") trackEvent("mql_created", { form_name: formName });
      if (funnelStage === "meeting_requested" || funnelStage === "checkout_requested") trackEvent("sql_created", { form_name: formName });

      trackEvent("form_submit_success", { form_name: formName });
      form.reset();
      status.textContent = dict["form.ok"];
      status.classList.add("is-success");

      const successTarget = form.getAttribute("data-success-reveal") || "";
      if (successTarget) {
        const revealEl = document.querySelector(successTarget);
        if (revealEl) revealEl.hidden = false;
      }

      if (form.hasAttribute("data-booking-form")) {
        form.querySelectorAll("[data-slot-button]").forEach((slotBtn) => slotBtn.classList.remove("is-selected"));
      }
    } catch (_err) {
      trackEvent("form_submit_failed", { form_name: form.getAttribute("name") || "contact" });
      if (_err?.message === "slot-required") status.textContent = "Please select a preferred time slot before submitting.";
      else if (_err?.message === "rate-limited") status.textContent = "Please wait a few seconds before submitting again.";
      else if (_err?.message === "bot-detected") status.textContent = "Submission blocked. Please retry.";
      else if (_err?.message === "turnstile-required") status.textContent = "Please complete verification before submitting.";
      else status.textContent = dict["form.fail"];
      status.classList.add("is-error");
    } finally {
      btn.disabled = false;
      btn.textContent = defaultLabel;
    }
  });
});

