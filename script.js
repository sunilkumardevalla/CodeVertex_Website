const renderBlogIndex = (posts, rootEl, activeCategory = "all", query = "", activeTag = "") => {
  if (!rootEl) return;
  const normalizedQuery = String(query || "").trim().toLowerCase();
  const normalizedTag = String(activeTag || "").trim().toLowerCase();
  const filtered = posts.filter((post) => {
    const categoryOk = activeCategory === "all" || post.category === activeCategory;
    if (!categoryOk) return false;
    if (normalizedTag) {
      const tagOk = (post.tags || []).some((tag) => String(tag).toLowerCase() === normalizedTag);
      if (!tagOk) return false;
    }
    if (!normalizedQuery) return true;
    const haystack = [post.title, post.excerpt, post.category, ...(post.tags || [])].join(" ").toLowerCase();
    return haystack.includes(normalizedQuery);
  });

  if (!filtered.length) {
    rootEl.innerHTML = `<article class="card reveal"><h3>No matching articles</h3><p>Try a different category or keyword.</p><a class="inline-link" href="blog.html">Reset filters</a></article>`;
    return;
  }

  rootEl.innerHTML = filtered.map((post) => `
    <article class="card reveal">
      <p class="kicker">${post.category} · ${post.read_time}</p>
      <h3>${post.title}</h3>
      <p>${post.excerpt}</p>
      <a class="inline-link" href="${post.url}">Read article</a>
    </article>
  `).join("");
};

const renderRelatedPosts = (posts) => {
  const rootEl = document.querySelector("[data-related-posts]");
  const slug = document.body.getAttribute("data-blog-slug") || "";
  if (!rootEl || !slug) return;

  const current = posts.find((p) => p.slug === slug);
  if (!current) return;

  const scorePost = (candidate) => {
    let score = 0;
    if (candidate.category === current.category) score += 3;
    const tagsA = new Set(current.tags || []);
    (candidate.tags || []).forEach((tag) => {
      if (tagsA.has(tag)) score += 1;
    });
    return score;
  };

  const related = posts
    .filter((p) => p.slug !== slug)
    .map((p) => ({ ...p, _score: scorePost(p) }))
    .sort((a, b) => b._score - a._score)
    .slice(0, 3);

  rootEl.innerHTML = related.map((post) => `
    <article class="card reveal">
      <p class="kicker">${post.category} · ${post.read_time}</p>
      <h3>${post.title}</h3>
      <p>${post.excerpt}</p>
      <a class="inline-link" href="${post.url}">Read article</a>
    </article>
  `).join("");
};

const blogIndexRoot = document.querySelector("[data-blog-index]");
const relatedPostsRoot = document.querySelector("[data-related-posts]");
if (blogIndexRoot || relatedPostsRoot) {
  fetch("assets/data/blog-posts.json", { cache: "no-store" })
    .then((res) => res.ok ? res.json() : Promise.reject(new Error("blog-posts-failed")))
    .then((data) => {
      const posts = Array.isArray(data?.posts) ? data.posts : [];

      if (blogIndexRoot) {
        const filterEl = document.querySelector("[data-blog-filter]");
        const searchEl = document.querySelector("[data-blog-search]");

        const categories = ["all", ...new Set(posts.map((p) => p.category).filter(Boolean))];
        if (filterEl) {
          filterEl.innerHTML = categories.map((category) => {
            const label = category === "all" ? "All categories" : category;
            return `<option value="${category}">${label}</option>`;
          }).join("");
        }

        const urlParams = new URLSearchParams(window.location.search);
        const presetCategory = urlParams.get("category") || "all";
        const presetQuery = urlParams.get("q") || "";
        const presetTag = urlParams.get("tag") || "";

        if (filterEl && [...filterEl.options].some((opt) => opt.value === presetCategory)) filterEl.value = presetCategory;
        if (searchEl && presetQuery) searchEl.value = presetQuery;

        const runRender = () => {
          const activeCategory = filterEl ? filterEl.value : "all";
          const query = searchEl ? searchEl.value : "";
          renderBlogIndex(posts, blogIndexRoot, activeCategory, query, presetTag);
        };

        runRender();
        if (filterEl) {
          filterEl.addEventListener("change", () => {
            runRender();
            trackEvent("blog_filter_changed", { category: filterEl.value });
          });
        }
        if (searchEl) {
          searchEl.addEventListener("input", () => {
            runRender();
            trackEvent("blog_search_used", { query_length: String(searchEl.value.length) });
          });
        }

        trackEvent("blog_index_loaded", { post_count: String(posts.length) });
      }

      if (relatedPostsRoot) {
        renderRelatedPosts(posts);
        trackEvent("blog_related_loaded", { post_count: String(posts.length) });
      }
    })
    .catch(() => {
      if (blogIndexRoot) {
        blogIndexRoot.innerHTML = `<article class="card reveal"><h3>Blog feed unavailable</h3><p>Unable to load blog catalog right now.</p><a class="inline-link" href="insights.html">Open insights</a></article>`;
      }
      if (relatedPostsRoot) {
        relatedPostsRoot.innerHTML = `<article class="card reveal"><h3>Related content unavailable</h3><p>Open the blog index for additional resources.</p><a class="inline-link" href="blog.html">Open blog index</a></article>`;
      }
      trackEvent("blog_feed_failed");
    });
}

const initBlogMediaExperience = () => {
  const audioBtn = document.querySelector("[data-blog-audio-toggle]");
  const audioSource = document.querySelector("[data-blog-audio-source]");

  if (audioBtn && audioSource) {
    let speaking = false;
    const stopSpeech = () => {
      window.speechSynthesis.cancel();
      speaking = false;
      audioBtn.textContent = "Play audio summary";
      audioBtn.setAttribute("aria-pressed", "false");
    };

    audioBtn.addEventListener("click", () => {
      if (!("speechSynthesis" in window)) {
        audioBtn.textContent = "Audio not supported on this browser";
        return;
      }

      if (speaking) {
        stopSpeech();
        return;
      }

      const utterance = new SpeechSynthesisUtterance(audioSource.textContent.trim());
      utterance.rate = 1;
      utterance.pitch = 1;
      utterance.onend = stopSpeech;
      utterance.onerror = stopSpeech;

      speaking = true;
      audioBtn.textContent = "Stop audio summary";
      audioBtn.setAttribute("aria-pressed", "true");
      window.speechSynthesis.cancel();
      window.speechSynthesis.speak(utterance);
      trackEvent("blog_audio_play");
    });

    window.addEventListener("beforeunload", () => {
      if (speaking) window.speechSynthesis.cancel();
    });
  }

  document.querySelectorAll("[data-reel-toggle]").forEach((button) => {
    button.addEventListener("click", () => {
      const card = button.closest("[data-reel]");
      if (!card) return;
      const video = card.querySelector("[data-reel-video]");
      if (!video) return;

      const isNowPlaying = video.paused;
      document.querySelectorAll("[data-reel-video]").forEach((v) => {
        if (v !== video) v.pause();
      });
      document.querySelectorAll("[data-reel]").forEach((c) => c.classList.remove("is-playing"));
      document.querySelectorAll("[data-reel-toggle]").forEach((b) => {
        if (b !== button) b.textContent = "Play reel";
      });

      if (isNowPlaying) {
        video.play().catch(() => {});
        card.classList.add("is-playing");
        button.textContent = "Pause reel";
        trackEvent("blog_reel_toggle", { state: "play" });
      } else {
        video.pause();
        card.classList.remove("is-playing");
        button.textContent = "Play reel";
        trackEvent("blog_reel_toggle", { state: "pause" });
      }
    });
  });
};

