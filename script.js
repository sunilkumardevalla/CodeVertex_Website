  document.body.appendChild(wrapper);

  const toggle = wrapper.querySelector(".cv-chat-toggle");
  const panel = wrapper.querySelector(".cv-chat-panel");
  const form = wrapper.querySelector("[data-chat-form]");
  const status = wrapper.querySelector("[data-chat-status]");

  if (toggle && panel) {
    toggle.addEventListener("click", () => {
      const open = !panel.hidden;
      panel.hidden = open;
      toggle.setAttribute("aria-expanded", String(!open));
    });
  }

  if (form) {
    form.addEventListener("submit", async (e) => {
      e.preventDefault();
      const webhook = window.CV_MARKETING_WEBHOOK || document.querySelector("meta[name='cv-marketing-webhook']")?.content || "";
      const payload = Object.fromEntries(new FormData(form).entries());
      payload.lead_source = "website-chat-widget";
      payload.page = window.location.pathname;
      if (status) status.textContent = "Sending...";
      try {
        if (!webhook) throw new Error("chat-not-configured");
        await fetch(webhook, {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify(payload)
        });
        if (status) status.textContent = "Message sent. Team will follow up shortly.";
        form.reset();
        trackEvent("chat_qualifier_submit");
      } catch (err) {
        if (err?.message === "chat-not-configured" && status) status.textContent = "Chat not configured. Please use Contact page.";
        else if (status) status.textContent = "Could not send right now. Please use Contact page.";
        trackEvent("chat_qualifier_failed");
      }
    });
  }
