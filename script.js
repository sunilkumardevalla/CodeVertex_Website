document.querySelectorAll("form[data-qualify-form='true']").forEach((form) => {
  const stages = Array.from(form.querySelectorAll(".qual-stage"));
  const progress = Array.from(form.parentElement.querySelectorAll(".qual-progress .qual-step"));
  if (!stages.length) return;

  let current = 0;
  const setStage = (idx) => {
    current = Math.max(0, Math.min(idx, stages.length - 1));
    stages.forEach((stage, i) => stage.classList.toggle("is-active", i === current));
    progress.forEach((step, i) => step.classList.toggle("is-active", i <= current));
    trackEvent("qualification_step", { step: current + 1 });
  };

  const validateStage = (idx) => {
    const fields = Array.from(stages[idx].querySelectorAll("input, select, textarea"));
    const invalid = fields.find((field) => !field.checkValidity());
    if (invalid) {
      invalid.reportValidity();
      return false;
    }
    return true;
  };

  form.addEventListener("click", (e) => {
    const next = e.target.closest("[data-qual-next]");
    const prev = e.target.closest("[data-qual-prev]");
    if (next) {
      if (!validateStage(current)) return;
      setStage(current + 1);
    }
    if (prev) setStage(current - 1);
  });

  form.addEventListener("submit", (e) => {
    computeLeadRouting(form);
    if (!validateStage(current)) e.preventDefault();
  });

  form.addEventListener("change", () => {
    computeLeadRouting(form);
  });

  setStage(0);
  computeLeadRouting(form);
});

